SELECT
  e.id,
  e.hire_date,
  NULL,
  e.full_name,
  e.ad_account,
  e.gender,
  e.mobile,
  e.company_email,
  e.personal_email,
  e.marital_status,
  e.political_affiliation,
  e.highest_education,
  e.highest_education_grad_date,
  e.fertility_status,
  e.ethnicity,
  e.hobbies,
  e.nationality,
  e.household_type,
  e.household_location,
  e.party_org_transferred,
  e.work_start_date,
  e.wechat,
  e.office_phone,
  e.office_extension,
  e.home_phone,
  e.id_card_address,
  e.residence_address,
  e.emergency_contact_name,
  e.emergency_contact_phone,
  e.emergency_contact_relation,
  e.recruitment_channel,
  e.recruitment_channel_detail,
  e.group_seniority_start_date,
  e.hire_date,
  e.status,
  e.created_at,
  e.updated_at,
  e.created_by,
  e.updated_by
FROM employee e
WHERE NOT EXISTS (
  SELECT 1
  FROM employee_master_version v
  WHERE v.employee_id = e.id
);

