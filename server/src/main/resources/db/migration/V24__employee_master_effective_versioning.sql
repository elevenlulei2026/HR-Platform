  full_name,
  ad_account,
  gender,
  mobile,
  company_email,
  personal_email,
  marital_status,
  political_affiliation,
  highest_education,
  highest_education_grad_date,
  fertility_status,
  ethnicity,
  hobbies,
  nationality,
  household_type,
  household_location,
  party_org_transferred,
  work_start_date,
  wechat,
  office_phone,
  office_extension,
  home_phone,
  id_card_address,
